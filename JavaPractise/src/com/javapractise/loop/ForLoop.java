package com.javapractise.loop;

import java.util.Scanner;

public class ForLoop {
	public static void main(String[] args) {
		Scanner sc;
		sc= new Scanner(System.in);
			System.out.println("Enter any value");
			int a= sc.nextInt();
		for(int i=0;i<=a;i++) {
			System.out.println(i);
		}
		  
	}

}
